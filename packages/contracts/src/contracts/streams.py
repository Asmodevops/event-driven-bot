"""Имена JetStream-стримов, общие для всех сервисов.

Стрим хранит сообщения для пространства сабджектов, чтобы они переживали
перезапуск консьюмеров (доставка at-least-once). Каждый стрим объявляется
одинаково везде, где его трогают — объявление идемпотентно, поэтому кто из
сервисов стартует первым, тот его и создаёт.
"""

from typing import Final


class Streams:
    # Хранит tg.cmd.> — пишет gateway, читает user_service.
    COMMANDS: Final = "commands"

    # Хранит tg.send.> — пишет user_service, читает sender.
    OUTGOING: Final = "outgoing"

    # Хранит tg.dlq.> — «мёртвые» сообщения для ручного разбора.
    DLQ: Final = "dlq"
